s=int(input("Enter the value of first number : "))
t=int(input("Enter the value of second number : "))
def sum(s,t):
    return (s+t)
print("The sum of two numbers is : ",sum(s,t))
def minus(s,t):
    return (s-t)
print("The difference of two numbers is : ",minus(s,t))
def multiply(s,t):
    return (s*t)
print("The product of two numbers is : ",multiply(s,t))
def divide(s,t):
    return (s/t)
print("The division of two numbers is : ",divide(s,t))
